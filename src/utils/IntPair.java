package utils;

public class IntPair {
	
	private int x;
	private int y;
	
	public IntPair(int a, int b){
		x = a;
		y = b;
	}
	
	public int getY() {
		return y;
	}
	
	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public void setY(int y) {
		this.y = y;
	}
	
	

}
